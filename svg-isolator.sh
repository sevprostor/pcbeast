#!/bin/bash

# Проверяем наличие обязательных аргументов
if [ -z "$1" ] || [ -z "$2" ]; then
    echo "Ошибка: Неверные аргументы."
    echo "Использование: $0 <путь_к_файлу.svg> <размер_зазора_в_мм>"
    echo "Пример: $0 pcb_offset.svg 0.2"
    exit 1
fi

INPUT_FILE=$(realpath "$1")
if [ ! -f "$INPUT_FILE" ]; then
    echo "Ошибка: Файл '$INPUT_FILE' не найден."
    exit 1
fi

# Преобразуем входной путь в абсолютный
INPUT_FILE=$(realpath "$1")
if [ ! -f "$INPUT_FILE" ]; then
    echo "Ошибка: Файл '$INPUT_FILE' не найден."
    exit 1
fi

OFFSET_MM="$2"

# Генерируем имя выходного файла и ТОЖЕ делаем путь к нему абсолютным
OUTPUT_DIR=$(dirname "$INPUT_FILE")
OUTPUT_NAME=$(basename "${INPUT_FILE%.*}_isolated.svg")
OUTPUT_FILE="$OUTPUT_DIR/$OUTPUT_NAME"

echo "=================================================="
echo "   Генерация сплошной обводки и совмещение путей"
echo "=================================================="
echo "Входной файл (абс.):  $INPUT_FILE"
echo "Выходной файл (абс.): $OUTPUT_FILE"

# ------------------------------------------------------------------------------
# АВТОМАТИЧЕСКИЙ РАСЧЕТ МАСШТАБА (ПИКСЕЛИ -> ММ)
# ------------------------------------------------------------------------------
# Извлекаем ширину холста в мм и в пикселях из тега <svg>, чтобы найти точный шаг
SVG_WIDTH_MM=$(grep -oE 'width="[0-9.]+(mm)?' "$INPUT_FILE" | head -n 1 | tr -d 'width="m')
VIEWBOX_WIDTH=$(grep -oE 'viewBox="[0-9.-]+ [0-9.-]+ [0-9.]+' "$INPUT_FILE" | head -n 1 | awk '{print $3}')

# Если CAD-система не прописала ширину в явных мм, используем стандартный шаг Inkscape (96 DPI)
if [ -z "$SVG_WIDTH_MM" ] || [ -z "$VIEWBOX_WIDTH" ]; then
    # Дефолтный коэффициент: 1 мм = 3.779527 px
    PX_PER_MM="3.779527"
    echo "--> Предупреждение: Масштаб не найден в файле. Используем дефолтные 96 DPI."
else
    # Вычисляем сколько пикселей viewBox содержится в одном физическом миллиметре платы
    PX_PER_MM=$(echo "scale=6; $VIEWBOX_WIDTH / $SVG_WIDTH_MM" | bc)
    echo "--> Обнаружен масштаб документа: 1 мм = $PX_PER_MM px (viewBox)"
fi

# Обводка идет в обе стороны, умножаем припуск на 2
STROKE_WIDTH_MM=$(echo "scale=4; $OFFSET_MM * 2" | bc)

# Переводим миллиметры в точные пиксели внутреннего масштаба вашего файла
STROKE_WIDTH_PX=$(echo "scale=4; $STROKE_WIDTH_MM * $PX_PER_MM" | bc)

echo "Припуск:       +$OFFSET_MM мм"
echo "Толщина линии: $STROKE_WIDTH_PX px (масштабировано)"
echo "--------------------------------------------------"
echo "--> Наращивание точной геометрии..."

# ==============================================================================
# ШАГ 1: ГЕНЕРАЦИЯ ОБВОДКИ (Используем абсолютные пути)
# ==============================================================================
ACTIONS_STEP1="select-all;\
object-set-property:stroke,#000000;\
object-set-property:stroke-width,${STROKE_WIDTH_PX}px;\
object-set-property:stroke-linejoin,round;\
object-stroke-to-path;\
selection-ungroup;\
path-break-apart;\
select-clear;\
select-all;\
path-union;\
export-filename:$OUTPUT_FILE;\
export-do"

echo "--> Шаг 1: Создание файла обводки..."
inkscape "$INPUT_FILE" --actions="$ACTIONS_STEP1"

# ==============================================================================
# ШАГ 2: ИМПОРТ ИСХОДНОЙ ПЛАТЫ В ПОЛУЧЕННЫЙ ФАЙЛ ОБВОДКИ
# ==============================================================================
if [ -f "$OUTPUT_FILE" ]; then
    echo "--> Шаг 2: Извлечение платы и вставка в файл обводки..."

    # AWK пропускает первую строку (XML-декларацию), открывающий тег <svg> 
    # и последнюю строчку </svg>, забирая строго внутренности платы.
    RAW_PCB=$(awk '
        /<\/svg>/ {inside=0}
        inside {print}
        /<svg/ {inside=1}
    ' "$INPUT_FILE")

    # Удаляем последнюю строчку </svg> из файла обводки (созданного на Шаге 1)
    sed -i '$d' "$OUTPUT_FILE"
    
    # Дописываем оригинальную плату
    echo "$RAW_PCB" >> "$OUTPUT_FILE"
    
    # Возвращаем закрывающий тег на место
    echo "</svg>" >> "$OUTPUT_FILE"

    echo "--> Успешно! Геометрия платы объединена в одном файле."
else
    echo "Ошибка: Не удалось найти файл обводки после Шага 1."
    exit 1
fi

# ==============================================================================
# ШАГ 3: ФИНАЛЬНОЕ ВЫРЕЗАНИЕ (РАЗНОСТЬ ПУТЕЙ)
# ==============================================================================
if [ -f "$OUTPUT_FILE" ]; then
    echo "--> Шаг 3: Вырезание основы из кокона обводки..."

    # 1. select-all — выделяем оба объекта (кокон обводки и вставленную плату).
    # 2. path-difference — Inkscape вычитает верхнюю плату из нижнего кокона.
    #    В результате останется только чистая "выворотка" (изоляционная дорожка).
    # 3. export-filename... ; export-do — перезаписываем финальный результат.
    ACTIONS_STEP3="select-all;path-difference;export-filename:$OUTPUT_FILE;export-do"

    inkscape "$OUTPUT_FILE" --actions="$ACTIONS_STEP3"
else
    echo "Ошибка: Целевой файл не найден перед Шагом 3."
    exit 1
fi

# ==============================================================================
# КОНЕЦ КОНВЕЙЕРА
# ==============================================================================
if [ -f "$OUTPUT_FILE" ]; then
    echo "=================================================="
    echo "   УСПЕХ! Изоляционный контур полностью готов!"
    echo "   Результат сохранен: $OUTPUT_FILE"
    echo "=================================================="
    exit 0
else
    echo "Ошибка на финальном этапе вырезания геометрии."
    exit 1
fi